
package com.ealen.yahoo.weather.forecast;

public class Condition {

    private String code;
    private String date;
    private String temp;
    private String text;

    /**
     * 
     * @return
     *     The code
     */
    public String getCode() {
        return code;
    }

    /**
     *
     * @return
     *     The date
     */
    public String getDate() {
        return date;
    }


    /**
     *
     * @return
     *     The temp
     */
    public String getTemp() {
        return temp;
    }


    /**
     * 
     * @return
     *     The text
     */
    public String getText() {
        return text;
    }


}
